# получение top-n популярных слов
def get_top_n_words(freq_dict: dict, top_n: int) -> list:
    freq = list(freq_dict.items())
    freq_s = sorted(freq, key=lambda num: num[1], reverse=True)

    top = [freq_s[i][0] for i in range(top_n)]
    return top

